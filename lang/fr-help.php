<h2>Aide</h2>
<p>
  
  Sert à faciliter l'utilisation du gestionnaire de médias.
  
</p>
<h3 style="font-size:1.3em;font-weight:bold;padding:10px 0 10px 0">Améliorations apportées</h3>
<ul>
<li>Visualisation de tous les dossiers, plutôt qu'une simple liste selectionnable.</li>
<li>Accès aux dossiers par simple clic.</li>
<li>Glisser-déposer des fichiers dans un dossier.</li>
</ul>
</p>

<h3 style="font-size:1.3em;font-weight:bold;padding:10px 0 10px 0">Comment est réalisé / Que fait ce plugin ?</h3>
<p>
  Ce plugin n'apporte pas grand chose mise à part une interface améliorée par Javascript.
<ul>
  <li>
    Les dossiers visibles sont ceux présents dans la liste "Dossier".
  </li>
  <li>
    Lorsque vous selectionner un dossier, la liste de sélection des dossiers est mise à jour.
  </li>
  <li>
    Lorsque vous faites un glissé-déposé, la case correpondant au fichier est  cochée et la liste de sélection est mise à jour; finalement on emet un évenement de clic sur le bouton de validation.
  </li>
</ul>
</p>



